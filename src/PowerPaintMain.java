import powerpaint.gui.PowerPaintFrame;

/**
 * Runs PowerPaint by instantiating and starting the PowerPaintGUI.
 * @author Mohammad Juma
 * @version 10-30-2012
 */
public final class PowerPaintMain
{
  /**
   * Private constructor, to prevent instantiation of this class.
   */
  private PowerPaintMain()
  {
    // do nothing.
  }
  
  /**
   * The main method, invokes the PowerPaint GUI. Command line
   * arguments are ignored.
   * 
   * @param the_args Command line arguments.
   */
  public static void main(final String[] the_args)
  {
    final PowerPaintFrame gui = new PowerPaintFrame();
    gui.start();
  }
}
